//=============================================================================
//コンストラクタ
//=============================================================================
//[input]
//	pName:データ名
//	vPos:設定する座標
//	pResFile:敵の動きのファイル
//=============================================================================
CEnemyRabbit::CEnemyRabbit( const char *pName, Math::Vector3D vPos, File::IResourceFile *pResFile )  
:CEnemy( pName, vPos, pResFile )
{
	m_TypeName = "4xG";//ウサギ
	m_StTypeName = "ウサギ";

	float Scale = 0.5f;
	
	m_vScale.Set( Scale, Scale, Scale );
	
	m_vInitScale = m_vScale;
	
}


//=============================================================================
//初期化
//=============================================================================
void CEnemyRabbit::Init()
{
	CEnemy::Init();
	
	SetRootKey();
	
}

//=============================================================================
//処理
//=============================================================================
void CEnemyRabbit::Exec()
{
	m_vCatchPos = m_vPos + Math::Vector3D( 0, 0.5f, 0 ) + m_vFront * 0.8f;
	
	CEnemy::Exec();	
}

//=============================================================================
//移動処理
//=============================================================================
void CEnemyRabbit::MoveExec()
{
	MoveRoot();
}


