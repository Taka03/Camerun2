//=============================================================================
//コンストラクタ
//=============================================================================
//[input]
//	pName:データ名
//	vPos:設定する座標
//	pResFile:敵の動きのファイル
//=============================================================================
CEnemySkyObj::CEnemySkyObj( const char *pName, Math::Vector3D vPos, File::IResourceFile *pResFile )  
:CEnemy( pName, vPos, pResFile )
{
	m_TypeName = "we5y";//ジョウシ
	m_StTypeName = "テイエン";
	
	m_IsCatchable = false;
	m_IsExistAnim = false;

}


//=============================================================================
//初期化
//=============================================================================
void CEnemySkyObj::Init()
{
	CEnemy::Init();
	
}

//=============================================================================
//処理
//=============================================================================
void CEnemySkyObj::Exec()
{
	m_vCatchPos = m_vPos + Math::Vector3D( 0, -0.5f, 0 ) + m_vFront * 0.8f;
	
	CEnemy::Exec();	
}

